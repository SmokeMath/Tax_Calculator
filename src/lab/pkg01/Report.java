    private void btnEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmailActionPerformed
        String emailTo ="";
        String emailFrom ="javatest@curingtontechnology.com";
        String message = txtReport.getText();
        String subject = "Java";
        String smtpServer = "smtp.curingtontechnology.com";
        String username = emailFrom;
        String password = "*******";
        
        emailTo = JOptionPane.showInputDialog(null,"Enter E-mail Address","E-mail",JOptionPane.QUESTION_MESSAGE);
        
        if (emailTo != "" && emailTo != null)
       {
            try
            {
                Properties props = System.getProperties();
                
                props.put("mail.transport.protocol","smtp");
                props.put("mail.smtp.starttls.enable","true");
                props.put("mail.smtp.host",smtpServer);
                props.put("mail.smtp.auth","true");
                props.put("mail.smtp.port","587");
                
                Session session = Session.getInstance(props, new javax.mail.Authenticator() {
                   @Override 
                   protected PasswordAuthentication getPasswordAuthentication() {
                       return new PasswordAuthentication(username, password);
                   }
                });
                
                Message msg = new MimeMessage(session);
                
                msg.setFrom(new InternetAddress(emailFrom));
                msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(emailTo, false));
                msg.setSubject(subject);
                msg.setText(message);
                
                msg.setHeader("MyEmail", "Krunkalicious Technology");
                msg.setSentDate(new Date());
                
                Transport.send(msg);
                
                JOptionPane.showMessageDialog(null,"E-Mail Sent Successfully!");
                }   
                catch (Exception e)
                {
                
                }    
            
               }
            else
            {
                JOptionPane.showMessageDialog(null, "Bad E-mail");
            }
            
        
    }//GEN-LAST:event_btnEmailActionPerformed

